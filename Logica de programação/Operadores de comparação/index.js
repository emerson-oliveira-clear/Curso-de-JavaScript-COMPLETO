
/*
> - maior
< - menor
>= - maior ou igual
<= - menor ou igual
== - igual
=== - estritamente igual (valor e tipo)
!= - diferente
!== - estritamente diferente (valor e tipo)
*/

console.log(10 > 5)
console.log(10 < 5)
console.log(10 >= 5)
console.log(10 <= 5)
console.log(10 == 5)
console.log(10 === 5)
console.log(10 != 5)
console.log(10 !== 5)

